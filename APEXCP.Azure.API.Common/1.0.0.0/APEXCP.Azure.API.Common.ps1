<#
.SYNOPSIS
Set url format according to ip protocal

.PARAMETER URL
Required. Rest API URL

#>
function Get-Url{
    param(
        [String] $Server,

        [String] $Uri
    )
    if ( -not ($Server -match $IPV6_ADDR_PATTERN)) {
        return "https://" + $Server + $Uri
    }
    else{
        return "https://" + "[" + $Server + "]" + $Uri
    }
}

